let opção = Number(prompt("digite um numero"))



switch(opção){
    
        case 1:
        console.log("iniciar jogo")
        break;
   
        case 2:
        console.log("carregar jogo")
        break;
   
        case 3:
        console.log("sair")
        break;

        default:
            console.log("opção invalida")
}
